function checkPort(name, port)
{
	var msg = "";
	var portNum = parseInt(port,10);

	if (port.length == 0)
	{
		msg = name + " " + "must not be blank.\n";
		alert(msg);
		return false;
	}
	for(i=0;i<port.length;i++)
	{
		c=port.charAt(i);
		if("0123456789".indexOf(c,0)<0)
		{
			msg = "Invalid " + name;
			alert(msg);			
			return false;
		}	
	}
	if (isNaN(portNum) || portNum < 1 || portNum > 65535)
	{
		msg = "Invalid " + name + ", must be in the range 1-65535.\n";
		alert(msg);
		return false;
	}
	return true;
}

